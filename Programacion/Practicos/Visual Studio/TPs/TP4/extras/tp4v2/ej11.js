/*11. El índice de masa corporal (IMC) se calcula de la siguiente manera: bmi = peso en Kg / 
(altura x altura) en m2. Escribe una función que calcule bmi. El IMC se utiliza para definir de 
forma amplia diferentes grupos de peso en adultos de 20 años o más. Compruebe si una persona tiene un peso inferior al normal, normal, con sobrepeso u obeso según la 
información que se proporciona a continuación.*/

function IMC(Kg, Altura) {
  bmi = Kg / (Altura * Altura);
}
